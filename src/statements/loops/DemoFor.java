package statements.loops;

public class DemoFor {

    public static void main(String[] args) {

        //Count of digits
        int a = 5588;
        int count = 0;
        for(a = 5588; a!=0; a=a/10){
            count++;
        }
        System.out.println("Count of digits : "+ count);

        //Print even digits from number


    }
}
